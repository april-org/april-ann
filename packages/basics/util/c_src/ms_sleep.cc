#include "ms_sleep.h"

namespace april_utils {

  void msleep(unsigned long milisec) {
    struct timespec req={0,0};
    time_t sec=(int)(milisec/1000);
    milisec=milisec-(sec*1000);
    req.tv_sec=sec;
    req.tv_nsec=milisec*1000000L;
    while(nanosleep(&req,&req)==-1) {
      continue;
    }
  }

}
